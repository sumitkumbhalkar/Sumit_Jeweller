export const calculateFinalPrice = (
  weight: number,
  purity: string,
  makingCharges: number,
  goldRates: GoldRate[]
): number => {
  const rate = goldRates.find((r) => r.purity === purity)?.pricePerGram || 0;
  const goldValue = weight * rate;
  const gst = goldValue * 0.03; // 3% GST
  return Math.round(goldValue + makingCharges + gst);
};
